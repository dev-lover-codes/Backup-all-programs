fun main() {
    val x = 11
    val y = 9

    // Addition (+)
    println(x + y) // Output: 20

    // Subtraction (-)
    println(x - y) // Output: 2

    // Multiplication (*)
    println(x * y) // Output: 99

    // Division (/)

    println(x / y) // Output: 1 (Integer division rounds down)

    // Modulo (%)
    println(x % y) // Output: 2 (The remainder of 11 / 9)
    //comparision operator
    println(x == y)
}
